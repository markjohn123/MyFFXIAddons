windower.register_event('incoming chunk',function(id,original,modified,is_blocked,is_injected)
    if id == 0x34 then
        local menu_id = original:byte(45)+original:byte(46)*256
        if menu_id >= 8700 and menu_id <= 8704 then     -- Homepoint
            return original:sub(1,12)..string.char(0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0xF7,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0x03)..original:sub(29)
        elseif menu_id >= 8500 and menu_id <= 8501 then -- Survival Guide
            return original:sub(1,20)..string.char(0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0x03,0x00,0x00,0x00)..original:sub(37)
        elseif menu_id >= 5000 and menu_id <= 5009 then -- Waypoint
            return original:sub(1,12)..string.char(0xFF,0x81,0xFF,0x00,0x00,0xFF,0xFF,0xFF,0xCF,0x03,0x00,0x00,0xDF,0x0F,0x00,0x00,0xCF,0x07,0x00,0x00,0xCF,0x07)..original:sub(35)
        end
    end
end)
